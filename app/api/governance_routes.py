def _read_json_file(path: Path) -> Dict[str, Any]:
    if not path.exists():
        raise HTTPException(status_code=404, detail=f"Required governance evidence file not found: {path}")
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)



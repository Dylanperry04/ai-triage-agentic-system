@router.post(
    "/governance/policy-checks",
    dependencies=[Depends(requires(authz.PERM_VIEW_AUDIT_LOG, "governance_policy_checks"))],
)
def run_governance_policy_checks():
    """Execute governance policy checks on the backend.

    Streamlit must not import/run the workflow orchestrator directly for
    protected actions. This route preserves the same executable checks while
    keeping workflow execution behind FastAPI auth/RBAC/audit.
    """
    from app.agents.orchestrator import run_workflow
    from app.governance.policy_checks import run_policy_checks, policy_red_team_probes

    return {
        "policy_results": run_policy_checks(run_workflow, settings),
        "red_team_results": policy_red_team_probes(),
        "scope": "backend_executed_policy_checks",
    }


class WandbLogRequest(BaseModel):
    policy_results: Dict[str, Any]
    red_team_results: Optional[Dict[str, Any]] = None
    model_metrics: Optional[Dict[str, Any]] = None
    project: str = Field(default="triage-governance", max_length=128)
    run_name: Optional[str] = Field(default="governance-policy-checks", max_length=128)
    mode: str = "online"

    @field_validator("mode")
    @classmethod
    def _mode_allowed(cls, v: str) -> str:
        mode = (v or "").strip().lower()
        if mode not in {"online", "offline"}:
            raise ValueError("mode must be 'online' or 'offline'")
        return mode


@router.get(
    "/governance/wandb-status",
    dependencies=[Depends(requires(authz.PERM_VIEW_AUDIT_LOG, "governance_wandb_status"))],
)
def governance_wandb_status():
    """Report non-secret backend W&B readiness for the Streamlit UI."""
    from app.governance.wandb_logging import wandb_available, wandb_configured
    from app.security.identity import (
        cloud_egress_allowed,
        local_credentialed_research_mode,
        patient_data_mode,
    )

    local_mode = local_credentialed_research_mode()
    available = wandb_available()
    configured = wandb_configured()
    return {
        "available": available,
        "configured_for_online": configured,
        "cloud_egress_allowed": cloud_egress_allowed(),
        "local_credentialed_research": local_mode,
        "patient_data_mode": patient_data_mode(),
        "offline_allowed_in_local_research": (
            not local_mode
            or __import__("os").environ.get("ALLOW_WANDB_OFFLINE_IN_LOCAL_RESEARCH", "").lower() == "true"
        ),
        "status": (
            "configured"
            if configured
            else "available_not_configured"
            if available
            else "not_installed"
        ),
    }


@router.post(
    "/governance/log-wandb",
    dependencies=[Depends(requires(authz.PERM_VIEW_AUDIT_LOG, "governance_log_wandb"))],
)
def governance_log_wandb(body: WandbLogRequest):
    """Perform optional W&B logging on the backend behind RBAC/audit."""
    from app.governance.wandb_logging import log_governance_run

    return log_governance_run(
        body.project,
        body.policy_results,
        body.red_team_results,
        body.model_metrics,
        run_name=body.run_name,
        mode=body.mode,
    )
